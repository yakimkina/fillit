#include "libft.h"

int			ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t	i;

	if (s1 == NULL || s2 == NULL)
		return (0);
	i = 0;
	while ((i < n) && (s1[i] == s2[i]))
		i++;
	if ((i == n) || ((s1[i - 1] == '\0') && (s2[i - 1] == '\0')
		&& (i != 0)))
		return (1);
	else
		return (0);
}
