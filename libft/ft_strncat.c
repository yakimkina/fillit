#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*str;

	str = s1;
	while (*str != '\0')
		str++;
	i = 0;
	while ((++i <= n) && (*s2 != '\0'))
	{
		*str = *s2;
		str++;
		s2++;
	}
	*str = '\0';
	return (s1);
}
