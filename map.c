#include "fillit.h"

int				size_of_map(int n)
{
	int			i;

	i = 2;
	while (i * i < n)
		i += 1;
	return (i);
}

int				free_map(char ***map, int i)
{
	while (i--)
		free((*map)[i]);
	free((*map));
	return (0);
}
